using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

namespace Jintori
{
    // --- Class Declaration ------------------------------------------------------------------------
    public class ScoreDisplay : MonoBehaviour
    {
        // --- Events -----------------------------------------------------------------------------------
        // --- Constants --------------------------------------------------------------------------------
        // --- Static Properties ------------------------------------------------------------------------
        // --- Static Methods ---------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------
        // --- Inspector --------------------------------------------------------------------------------
        [SerializeField]
        Text valueText;

        // --- Properties -------------------------------------------------------------------------------
        /// <summary> Current time, in seconds (0~999)  </summary>
        public long score { set { SetScore(value); } }

        // --- MonoBehaviour ----------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------	
        // --- Methods ----------------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------	
        public void Reset()
        {
            score = 0;
        }

        // -----------------------------------------------------------------------------------	
        void SetScore(long value)
        {
            valueText.text = string.Format("{0:##,###,###,##0}", value);
        }

    }
}