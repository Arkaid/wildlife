using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using Jintori;
using Jintori.SelectScreen;

// --- Class Declaration ------------------------------------------------------------------------
public class Test : MonoBehaviour 
{
    // --- Events -----------------------------------------------------------------------------------
    // --- Constants --------------------------------------------------------------------------------
    // --- Static Properties ------------------------------------------------------------------------
    // --- Static Methods ---------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------
    // --- Inspector --------------------------------------------------------------------------------

    // --- Properties -------------------------------------------------------------------------------
    [SerializeField]
    CharacterGrid test;

    // --- MonoBehaviour ----------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------	

	// --- Methods ----------------------------------------------------------------------------------
	// -----------------------------------------------------------------------------------	
    void Update()
    {
        if (Input.GetKeyDown(KeyCode.LeftArrow))
        {
            test.NextPage();
        }
        else if (Input.GetKeyDown(KeyCode.RightArrow))
        {
            test.PrevPage();

        }
    }
}
