using UnityEngine;
using UnityEngine.UI;

namespace IllogicGate.UI
{
    // --- Class Declaration ------------------------------------------------------------------------
    public class ColorToggle : Toggle
    {
        // --- Events -----------------------------------------------------------------------------------
        // --- Constants --------------------------------------------------------------------------------
        // --- Static Properties ------------------------------------------------------------------------
        // --- Static Methods ---------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------
        // --- Inspector --------------------------------------------------------------------------------
        [SerializeField]
        Color toggleOnColor = Color.gray;

        // --- Properties -------------------------------------------------------------------------------
        // --- Toggle -----------------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------
        protected override void Awake()
        {
            base.Awake();
            onValueChanged.AddListener(OnValueChanged);
        }
        // --- Methods ----------------------------------------------------------------------------------
        // -----------------------------------------------------------------------------------
        void OnValueChanged(bool isOn)
        {
            if (isOn)
                targetGraphic.color = toggleOnColor;
            else
                targetGraphic.color = colors.normalColor;
        }
    }
}